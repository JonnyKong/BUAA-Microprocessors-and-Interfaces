DATA SEGMENT
	DT1 DB 5CH
DATA ENDS

STACK SEGMENT STACK
	DB 100 DUP(0)
STACK ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA, SS:STACK
MAIN PROC
	MOV AX, DATA
	MOV DS, AX
	CALL DISP3
	MOV AH, 4CH
	INT 21H
MAIN ENDP
DISP PROC
	MOV DL, DT1
	MOV CL, 4
	SHR DL, CL
	CMP DL, 9
	JBE NEXT1
	ADD DL, 7
	NEXT1: ADD DL, 48
	MOV AH, 2
	INT 21H
	MOV DL, DT1
	AND DL, 0FH
	CMP DL, 9
	JBE NEXT2
	ADD DL, 7
	NEXT2: ADD DL, 48
	MOV AH, 2
	INT 21H
	MOV DL, 'H'
	MOV AH, 2
	INT 21H
	MOV DL, ' '
	MOV AH, 2
	INT 21H
	RET
DISP ENDP

DISP2 PROC
	MOV AL, DT1
	MOV AH, 0
	MOV BL, 100
	DIV BL
	MOV DL, AL
	ADD DL, 48
	PUSH AX	;SAVE RESIDUAL
	MOV AH, 2
	INT 21H
	POP AX
	MOV AL, AH
	MOV AH, 0
	MOV BL, 10
	DIV BL
	MOV DL, AL
	MOV AL, AH
	PUSH AX
	ADD DL, 48
	MOV AH, 2
	INT 21H
	POP DX
	ADD DL, 48
	MOV AH, 2
	INT 21H
	MOV DL, ' '
	MOV AH, 2
	INT 21H
	RET
DISP2 ENDP

DISP3 PROC
	MOV BL, DT1
	MOV CX, 8
	NEXT3: ROL BL, 1
	MOV DL, BL
	AND DL, 1
	ADD DL, 30H
	MOV AH, 2
	INT 21H
	LOOP NEXT3
	MOV DL, 'B'
	MOV AH, 2
	INT 21H
	MOV DL, ' '
	MOV AH, 2
	INT 21H
	RET
DISP3 ENDP

CODE ENDS
	END MAIN